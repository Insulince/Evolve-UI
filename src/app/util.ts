  public static generateRandomName(): string {
    let randomName: string = "";

    for (let i: number = 0; i < Util.NAME_LENGTH / 2; i++) {
      randomName += Util.WORDS[Math.floor(Math.random() * Util.WORDS.length)];
    }

    return randomName;
  }

  public static mutateName(name: string): string {
    const mutationPosition: number = Math.floor(Math.random() * (Util.NAME_LENGTH / 2)) * 2;

    return name.substr(0, mutationPosition) + Util.WORDS[Math.floor(Math.random() * Util.WORDS.length)] + name.substr(mutationPosition + 2);
  }
